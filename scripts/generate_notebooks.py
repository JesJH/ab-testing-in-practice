        '',
        'The simplest approach: divide α by the number of tests.',
        '',
        '$$\\alpha_{\\text{adjusted}} = \\frac{\\alpha}{k}$$',
        '',
        'A test is significant only if its p-value is below the adjusted threshold.',
        '',
